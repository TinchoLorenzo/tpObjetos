
public class Lote {

	private int tamano;
	private String tipo;
	private int id_Lote;
	
	public int getTamano() {
		return tamano;
	}
	
	public void setTamano(int tamano) {
		this.tamano = tamano;
	}
	
	public String getTipo() {
		return tipo;
	}
	
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	public int getId_Lote() {
		return id_Lote;
	}
	
	public void setId_Lote(int id_Lote) {
		this.id_Lote = id_Lote;
	}
	
	
}
